Geodeasy is an open source tool for a set of basic and complicated geodetic
computations. Although all of its code is available, you can still use the
REST API instead.

Geodeasy available tools:

<a href="#">Destination Calculation</a>
<a href="#">Great Circle Distance Calculation</a>
<a href="#">Lat/Lon to XYZ (ECEF) Conversions</a>
<a href="#">Geographic - Universal Transverse Mercator Conversions</a>
<a href="#">Geographic - Transverse Mercator Conversions</a>
<a href="#">Geographic - Lambert Conformal Conic Conversions</a>

